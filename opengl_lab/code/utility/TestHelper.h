    for (int i  = 0 ; i < result.size(); i++) {
      condition  &= result.at(i).size()== val.at(i).size();
      if (condition) {
	for (int j = 0; j < result.at(i).size(); j++) {
          condition &= (result.at(i).at(j) == val.at(i).at(j));
	}
      }
      if (!condition) {
	broken_i = i;
	break;
      }
    }
    
